void Adder(int32_t* A, int32_t *B, int32_t* RetVal, int32_t nLen)
{
    register uint32_t nCarry = 0;
    for (int32_t i = nLen; i >= 0; --i)
    {
        if ((A[i] + B[i]) >= 10)
        {
            if (nCarry > 0)
            {
                if (i == 0)
                    RetVal[i] = A[i] + B[i] + nCarry;
                else
                    RetVal[i] = (A[i] + B[i]) - (nCarry * 10) + nCarry;
            }
            else
            {
                if (i == 0)
                    RetVal[i] = A[i] + B[i] + nCarry;
                else
                {
                    nCarry = A[i] + B[i] % 10;
                    nCarry /= 10;
                    RetVal[i] = (A[i] + B[i]) - (nCarry * 10);
                }
            }
        }
        else if (((A[i] + B[i]) <= 10) && nCarry > 0)
        {
            RetVal[i] = (A[i] + B[i]) + nCarry;
            nCarry = 0;
        }
        else if (((A[i] + B[i]) <= 10))
            RetVal[i] = A[i] + B[i];
    }
}


